export type TranslationKey = keyof (typeof translations)['de'];

export function t(lang: Lang) {
  return translations[lang];
}

export function getMosqueName(mosque: { name: string; name_ar?: string | null; name_en?: string | null }, lang: Lang) {
  if (lang === 'ar' && mosque.name_ar) return mosque.name_ar;
  if (lang === 'en' && mosque.name_en) return mosque.name_en;
  return mosque.name;
}

export function getEventTitle(
  event: { title?: string | null; title_ar?: string | null; title_en?: string | null },
  lang: Lang
) {
  if (lang === 'ar' && event.title_ar) return event.title_ar;
  if (lang === 'en' && event.title_en) return event.title_en;
  return event.title ?? '';
}

export function getPrayerLabel(lang: Lang, name: PrayerName) {
  return translations[lang].prayers[name];
}

export function getLocationErrorMessage(tr: ReturnType<typeof t>, code: string | null): string | null {
  if (!code) return null;
  const map: Record<string, string> = {
    geolocation_unsupported: tr.geolocationUnsupported,
    permission_denied: tr.locationPermissionDenied,
    timeout: tr.locationTimeout,
    detect_failed: tr.locationDetectFailed,
    failed_prayer_times: tr.failedPrayerTimes,
  };
  return map[code] ?? code;
}

export function getAppBrandName(lang: Lang) {
  return lang === 'ar' ? translations[lang].appNameAr : translations[lang].appName;
}

export function setDocumentLanguage(lang: Lang) {
  if (typeof document === 'undefined') return;
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
  localStorage.setItem('lang', lang);
}

export function getStoredLanguage(): Lang {
  if (typeof window === 'undefined') return 'de';
  const stored = localStorage.getItem('lang');
  if (stored === 'ar' || stored === 'en' || stored === 'de') return stored;
  return 'de';
}
